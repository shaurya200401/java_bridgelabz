package Method.lvl2;
public class unitconvert3 {
    public static double convertFtoC(double f) { return (f - 32) * 5 / 9; }
    public static double convertCtoF(double c) { return (c * 9 / 5) + 32; }
    public static double convertLbToKg(double lb) { return lb * 0.453592; }
    public static double convertKgToLb(double kg) { return kg * 2.20462; }
    public static double convertGalToLitre(double g) { return g * 3.78541; }
    public static double convertLitreToGal(double l) { return l * 0.264172; }
}